def world():
    bgcolor("black")
    path.color("blue")

    for index in range(len(Mapa)):
        tile = Mapa[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, "white")


"Ruch do all"
def move():
    writer.undo()
    writer.write(state["score"])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if Mapa[index] == 1:
        Mapa[index] = 2
        state["score"] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, "yellow")

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, "red")

    update()
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


