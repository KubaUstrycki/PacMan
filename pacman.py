"tworzy okno"
def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


"odleglosci p"
def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    index = offset(point)

    if Mapa[index] == 0:
        return False

    index = offset(point + 19)

    if Mapa[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


